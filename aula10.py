"""
Concatenação de strings
É a operação de unir duas ou mais strings em uma única string.  
Pode ser feita usando o operador + ou a vírgula , no print.
"""

concatenacao1 = 'Olá' + ' ' + 'Mundo' + '!'
print(concatenacao1)

"""
Repetição de strings
É a operação de repetir uma string um determinado número de vezes.  
Pode ser feita usando o operador *.
"""

python_cinco_vezes = 'Python! ' * 5
print(python_cinco_vezes)