"""
Tipos int e float 
int são números inteiros,
O tipo int representa números inteiros, positivos ou negativos, sem parte decimal.
int sem sinal (unsigned) representa apenas números não negativos (0 e positivos).

float são números com ponto flutuante (números reais).
float representa qualquer número real, positivo ou negativo, com parte decimal.
float sem sinal (unsigned) representa apenas números reais não negativos (0.0 e positivos).
O ponto é usado como separador decimal (ex: 3.14, -0.001).
"""
