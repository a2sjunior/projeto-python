"""
Tipo de dado bool (boolean) 
Pode assumir apenas dois valores: 
True (verdadeiro) ou False (falso).
É usado para representar valores lógicos, como condições e resultados de comparações.
Existem vários operadores que retornam valores booleanos:

Operadores de comparação:
== (igual a)
!= (diferente de)
> (maior que)
< (menor que)
>= (maior ou igual a)
<= (menor ou igual a)(igual ou menor que)

Operadores de comparação retornam valores booleanos (True ou False).
São usados para comparar valores e expressões.
"""
print(10 == 10) # True
print(10 == 11) # False

print(type(10 == 10)) # <class 'bool'>
print(type(True)) # <class 'bool'>
print(type(False)) # <class 'bool'>


