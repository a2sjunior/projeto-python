"""
Operadores aritimécos em Python
São usados para realizar operações matemáticas comuns, como adição, subtração, multiplicação, divisão, entre outros.
"""

adicao = 10 + 5
print('Adição:', adicao)
subtracao = 10 - 5
print('Subtração:', subtracao)
multiplicacao = 10 * 5
print('Multiplicação:', multiplicacao)
divisao = 10 / 3
print('Divisão:', divisao)

divisao_inteira = 10 // 3.0
print('Divisão Inteira:', divisao_inteira)

resto_divisao = 10 % 3 #módulo
print('Resto da Divisão:', resto_divisao)

exponenciacao = 2 ** 10
print('Exponenciação:', exponenciacao)

raiz_quadrada = 81 ** (1/2)
print('Raiz Quadrada:', raiz_quadrada)

raiz_cubica = 27 ** (1/3)
print('Raiz Cúbica:', raiz_cubica)